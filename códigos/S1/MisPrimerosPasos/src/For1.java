
public class For1 {

	public static void main(String[] args) {
		
		for(int i=1;i<=10;i++) {
			System.out.print("\n\t i = "+i);
		}
		
		/*
		 * Usamos el ciclo for cuando sabemos el 
		 * número exacto de veces que queremos 
		 * que un bloque de código de repita.
		 * 
		 * Iteración significa repetición.
		 * */
		
	}

}
