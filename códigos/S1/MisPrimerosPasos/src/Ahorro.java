
public class Ahorro {

	public static void main(String[] args) {

		int ahorro=0; // Las sumas se inician en 0
		
		ahorro+=100; // ahorro = 100
		ahorro+=150; // ahorro = 250
		ahorro+=180; // ahorro = 430
		ahorro+=50; // ahorro = 480
		ahorro*=2; // ahorro = 960
		
		System.out.println("\n\t El ahorro final es: $"+ahorro);
		
	}

}
