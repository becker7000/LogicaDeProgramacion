
public class Strings1 {

	public static void main(String[] args) {
		
		String nombre="Erick";
		System.out.println("\n\t Mi nombre es: "+nombre);
		System.out.println("\n\t La primer letra es: "+nombre.charAt(0));
		System.out.println("\n\t Completo: "+nombre.concat(" Alvarez"));
		System.out.println("\n\t Tu nombre tiene "+nombre.length()+" letras");
		
	}

}
